#ifndef KEYCODES_H
#define KEYCODES_H

/* Control codes. */

#define CTRL_SPC	0x00    /* @ */
#define CTRL_A		0x01
#define CTRL_B		0x02
#define CTRL_C		0x03
#define CTRL_D		0x04
#define CTRL_E		0x05
#define CTRL_F		0x06
#define CTRL_G		0x07
#define CTRL_I		0x09
#define CTRL_J      0x0a
#define CTRL_K		0x0b
#define CTRL_L      0x0c
#define CTRL_M		0x0d
#define CTRL_N		0x0e
#define CTRL_O      0x0f
#define CTRL_P		0x10
#define CTRL_Q      0x11
#define CTRL_R      0x12
#define CTRL_S		0x13
#define CTRL_T		0x14
#define CTRL_U      0x15
#define CTRL_V		0x16
#define CTRL_W      0x17
#define CTRL_X		0x18
#define CTRL_Y		0x19
#define CTRL_Z      0x1a
#define CTRL_LBKT   0x1b    /* [ */
#define CTRL_BSLSH  0x1c    /* \ */
#define CTRL_RBKT   0x1d    /* ] */
#define CTRL_CRT    0x1e    /* ^ */
#define CTRL_USCR   0x1f    /* _ */
#define BKSPC	    0x7f

/* Control code aliases. */

#define ENTER		CTRL_M
#define ESC		    CTRL_LBKT

#endif
